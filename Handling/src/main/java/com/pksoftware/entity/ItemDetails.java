package com.pksoftware.entity;

public class ItemDetails {
	
	String itemname;
	String itemprize;
	String itemSelectDate;
	String itemMfd;
	String itemExp;
	String itemWeight;
	
	
	public String getItemname() {
		return itemname;
	}
	public void setItemname(String itemname) {
		this.itemname = itemname;
	}
	public String getItemprize() {
		return itemprize;
	}
	public void setItemprize(String itemprize) {
		this.itemprize = itemprize;
	}
	public String getItemSelectDate() {
		return itemSelectDate;
	}
	public void setItemSelectDate(String itemSelectDate) {
		this.itemSelectDate = itemSelectDate;
	}
	public String getItemMfd() {
		return itemMfd;
	}
	public void setItemMfd(String itemMfd) {
		this.itemMfd = itemMfd;
	}
	public String getItemExp() {
		return itemExp;
	}
	public void setItemExp(String itemExp) {
		this.itemExp = itemExp;
	}
	public String getItemWeight() {
		return itemWeight;
	}
	public void setItemWeight(String itemWeight) {
		this.itemWeight = itemWeight;
	}
}
